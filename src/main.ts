const numbers = [1, 3, 7, 9, 2];
const target = 11;

const findIndexesByTarget = (nums: number[], target: number) => {
  for (let i = 0; i < nums.length; i++) {
    for (let j = 1; j < nums.length; j++) {
      if (nums[i] + nums[j] === target) {
        return [i, j];
      }
    }
  }
  return null;
};

const result = findIndexesByTarget(numbers, target);
